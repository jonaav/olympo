    private void tbClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbClienteMouseClicked
        // TODO add your handling code here:
        int seleccion = tbCliente.rowAtPoint(evt.getPoint());
        
        txtIdCliente.setText(tbCliente.getValueAt(seleccion, 0)+"");
        txtDniCliente.setText(tbCliente.getValueAt(seleccion,1)+"");
        txtNombreCliente.setText(tbCliente.getValueAt(seleccion,2)+"");
        txtApellidosCliente.setText(tbCliente.getValueAt(seleccion,3)+"");
        txtCorreoCliente.setText(tbCliente.getValueAt(seleccion,4)+"");
        txtTelefonoCliente.setText(tbCliente.getValueAt(seleccion,5)+"");
        txtFechaCliente.setText(tbCliente.getValueAt(seleccion,6)+"");
        
    }//GEN-LAST:event_tbClienteMouseClicked

