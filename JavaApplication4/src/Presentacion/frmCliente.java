    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if(txtNombreCliente.getText().isEmpty() || txtApellidosCliente.getText().isEmpty() || 
                txtDniCliente.getText().isEmpty() || txtCorreoCliente.getText().isEmpty() || 
                txtTelefonoCliente.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Complete todos los campos");
        }else{
            EntCliente cliente = new EntCliente();
            cliente.setNombre(txtNombreCliente.getText());
            cliente.setApellidos(txtApellidosCliente.getText());
            cliente.setDni(txtDniCliente.getText());
            cliente.setCorreo(txtCorreoCliente.getText());
            cliente.setTelefono(txtTelefonoCliente.getText());
            cliente.setFecha(txtFechaCliente.getText());
            String mensaje = logcliente.guardarCliente(cliente);
            limpiar();
            listarCliente();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

